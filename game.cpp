void destroy_object(Object * obj)
{
  for (int i = 0; i < objects_count; i++)
  {
    if (objects[i] == obj)
    {
      deleted_objects[deleted_objects_count++] = obj;
      objects[i] = NULL;
      break;
    }
  }
}

