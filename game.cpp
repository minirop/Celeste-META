bool tile_flag_at(int x, int y, int w, int h, u8 flag)
{
  for (int i = max(0, floor(x / 8.f)); i <= min(15, (x + w - 1) / 8.f); i++)
  {
    for (int j = max(0, floor(y / 8.f)); j <= min(15, (y + h - 1) / 8.f); j++)
    {
      if (fget(tile_at(i, j), flag))
      {
        return true;
      }
    }
  }
  return false;
}

